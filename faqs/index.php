    </div>
    <div class="tab-box">
      <p class="tab-answer">To apply for sliding fee scale, patients and their families must provide the following documents to verify gross income:</br>
            - Most recent tax filing with the IRS and/or W2's from employer(s)</br>
            - A copy of Social Security or Disability Award Letter(s) or unemployment statement(s).</br>
            - Pay stubs from all employerers for the last 30-day period.</br>
            - If unemployed, no income, and living with others, we require a signed and dated letter from the person providing support of the patient.</br>
            - Driver's license</br>
            - Any other income that is direct deposited.</br>
        For more information on sliding fee scale, click <a href="/services/patient_resources.php">here</a>.
