	<h5>News Archive</h5>
	<h6>New Providers Join Health Center</h6>
	<h6>November 18, 2013</h6>
	<div class="archive-section">
		<p>Vermillion-Parke Community Health Center (VPCHC) welcomes Family Nurse Practitioner, Louwanna Wallace, and Licensed Clinical Social Worker, Tasha Lewis-Stevens, to the health center.</p>
		<p>Louwanna Wallace, FNP-C, is a Certified Family Nurse Practitioner with over 17 years of primary care experience.  She is a graduate of Indiana State University, where she earned her master&rsquo;s degree through the Family Nurse Practitioner program. </p>
		<p>Wallace has worked in a variety of clinical settings, which includes working as a clinical instructor/adjunct faculty, a nursing care manager and intensive care unit nurse at Union Hospital in Terre Haute, IN, and also as a Family Nurse Practitioner at EZ Care Clinic and Family Medical Center in Paris, IL.</p>
		<p> &ldquo;I enjoy working as a nurse practitioner in family practice because I have been able to develop long term relationships with patients and families and provide a medical home, to help guide patients through all aspects of their healthcare,&rdquo; stated Wallace. &nbsp;&ldquo;I like to make a difference in the lives of patients by providing preventive services and education, as well as taking care of acute and chronic medical problems. &nbsp;I also look forward to working in the area where I was raised.&rdquo;</p>
		<p>Wallace will provide patient care at the VPCHC Cayuga and Bloomingdale health centers.  She is excited to join the VPCHC team and looks forward to serving the community and providing care to patients.</p>
		<p>Also joining VPCHC is Tasha Lewis-Stevens, LCSW. Lewis-Stevens is a Licensed Clinical Social Worker (LCSW) and graduate of Indiana University, where she earned her Masters of Social Work, and completed undergraduate studies in psychology at the University of Indianapolis.</p>
		<p>As a behavioral health provider at VPCHC, Lewis-Stevens will serve and assist patients with behavioral health care treatment. Her past experience includes Access Center Supervisor, Therapist/Crisis Specialist and Clinical Therapist Supervisor at Hamilton Center, and as a Licensed Clinical Social Worker at Union Hospital in Terre Haute and Clinton. Lewis-Stevens enjoys practicing in behavioral health care because, &ldquo;I love making a positive difference in other's lives and helping them see and meet their potential.&rdquo; She will provide behavioral health services at the VPCHC Cayuga and Clinton health centers.</p>
		<p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC offers primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
