	<h6>Grand Opening Set for Bloomingdale Health Center</h6>
	<h6>February 15, 2013</h6>
	<div class="archive-section">
		<p>Vermillion-Parke Community Health Center (VPCHC) is hosting a Grand Opening celebration on Wednesday, February 27th, in honor of its new, recently opened health center located at 201 W. Academy St. in Bloomingdale, Ind.</p>
		<p>The Grand Opening will be held from 11:30 a.m. to 1:30 p.m., with a speaker ceremony beginning at 12:00 noon, followed by a ribbon cutting with the Parke County Chamber of Commerce. The public is welcome to tour the new health center facility and also meet the Bloomingdale medical providers, Dr. Eric Beachy and Family Nurse Practitioner, Carrie Cunningham.</p>
		<p>&ldquo;The opening of the Bloomingdale health center creates access to health care for patients in a town where there has been no medical provider.  The Town of Bloomingdale and Vermillion-Parke Community Health Center partnered together, creating the opportunity for access to health care, quality employment and dedication to this beautiful community,&rdquo; stated Elizabeth Burrows, health center CEO.</p>
		<p>The Bloomingdale health center opened on January 7, 2013.  The facility is comprised of 3,000 square feet and includes state of the art energy efficient features such as a multi-zone HVAC system, high quality insulation values in the roof and walls and energy efficient lighting.</p>
		<p>VPCHC was one of three health centers in Indiana to be awarded a New Access Point grant by Health and Human Services (HHS) in June 2012.  The grant is made possible by the health care law to expand community health centers.</p>
		<p>VPCHC is partnered with Performance Services, Inc., for the design and construction of the Bloomingdale Community Health Center. Performance Services is an Indianapolis design-build engineering company that specializes in constructing and renovating educational and healthcare facilities to deliver optimal environments through both the Guaranteed Energy Savings Contract and Design-Build procurement methods.</p>
		<p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
