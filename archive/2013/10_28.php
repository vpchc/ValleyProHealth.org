	<h5>News Archive</h5>
	<h6>New Medical and Behavioral Health Providers Join Health Center</h6>
	<h6>October 28, 2013</h6>
	<div class="archive-section">
		<p>The Vermillion-Parke Community Health Center (VPCHC) would like to welcome Family Practitioner, Aziz Abed, M.D., MPH, and behavioral health provider, Jan Warner, MAC, to its health center.</p>
		<p>Dr. Abed is a graduate of St. George&rsquo;s University School of Medicine, the University of Illinois at Chicago - School of Public Health, and completed undergraduate studies at University of Illinois at Chicago.</p>
		<p>Dr. Abed completed his medical residency training through Union Hospital Family Medicine Residency Program in Terre Haute. He also visited VPCHC on medical rotations during his residency.
		Dr. Abed enjoys practicing family medicine because, &ldquo;I am able to establish long-term relationships with patients and their families. I provide care to each patient to help further improve their daily lives by educating them to make decisions regarding their care.&rdquo; Other clinical interests include sports medicine.</p>
		<p>Prior to medical school, Dr. Abed served as Program Coordinator through the Chicago Health Corps. He worked as a Teaching Assistant at Kaplan Medical in Chicago, training and preparing future doctors in clinical situations, to take and pass the required United States Clinical Skills exam. Dr. Abed enjoys sports, including football and basketball, weight training, and spending time with family.</p>
		<p>Jan Warner is a Masters of Arts in Counseling graduate of Lincoln Christian University in Lincoln, IL, a graduate of St. Louis Christian College in St. Louis, and completed undergraduate studies in social work at the University of Missouri in St. Louis.</p>
		<p>As a behavioral health provider at VPCHC, Warner serves and assists patients with behavioral health care treatment. Her past experience includes the United States Air Force, Student Counselor at Richland Community College and Herington Counseling and Learning Alternatives in Decatur, IL, and Social Service Director at Pleasant Meadows Christian Village in Chrisman, IL. Warner&rsquo;s military experience and being a veteran of the Gulf war brings a unique understanding of the military lifestyle and the difficulties of military soldiers, veterans and their family&rsquo;s experience on a daily basis and during war time.  Warner is passionate about helping those who have served our country, and their families.Warner enjoys practicing in behavioral health care because, &ldquo;It gives me the opportunity to empower others, instill hope, offer education, and assist others with navigating their daily life and struggles.&rdquo;</p>
		<p>Warner enjoys spending time with her children and grandchildren, movies, making homemade cards, scrapbooking family pictures, and enjoys attending her son&rsquo;s and grandson&rsquo;s sporting events and school functions.</p>
		<p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
