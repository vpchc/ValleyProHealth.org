	<h5>News Archive</h5>
	<h6>Psychologist Earns Program Recognition</h6>
	<h6>December 9, 2013</h6>
	<div class="archive-section">
		<p>Vermillion-Parke Community Health Center (VPCHC) Behavioral Health Director, Dr. Julia Wernz, was recently recognized for her completion of service to the National Health Service Corps program.</p>
		<p>As a behavioral health provider, Dr. Wernz, HSPP, has been a practicing psychologist since 2007. Her past experience includes working with emotionally-disordered children and their families in an outpatient setting, as well as inpatient and health psychology settings. Dr. Wernz has practiced more as a generalist in the field for the past five years, treating patients with attention deficit, developmental, mood, anxiety and personality disorders, while working with medical providers to assist with patient treatment. Dr. Wernz created the behavioral health integration model for VPCHC. In October, she received the All Star Provider of the Year Award, awarded by the Indiana Primary Health Care Association.</p>
		<p>Upon completion of her National Health Service Corps (NHSC) program service, Dr. Wernz joins a group of over 40,000 clinicians, who have pursued a career in helping underserved populations.  According to the NHSC website, NHSC helps bring health care to those who need it most. Since 1972, the program has been building healthy communities by connecting primary health care providers to areas of the United States with limited access to care. More than 45,000 primary care medical, dental, and mental and behavioral health professionals have served in the National Health Service Corps since its inception.</p>
		<p>The NHSC is a Federal government program that is part of the U.S. Department of Health and Human Services. Specifically, the Corps is administered by the Health Resources and Services Administration (HRSA), Bureau of Clinician Recruitment and Service (BCRS).</p>
		<p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
