	<h5>News Archive</h5>
	<h6>Mobile Health Center to Provide Community Access to Health Care</h6>
	<h6>May 23, 2013</h6>
	<div class="archive-section">
		<p>Vermillion-Parkes Community Health Center (VPCHC) is partnering with Ivy Tech Community College to provide health care access through the VPCHC mobile health center, while on-site at Ivy Tech Community College&mdash;Wabash Valley&rsquo;s Parke County Learning Center (PCLC).</p>
		<p>The VPCHC mobile health center will be stationed at the PCLC on Tuesday, June 11th and June 18th, from 11:00 a.m. to 1:00 p.m., located on US41 North at 519 N. Lincoln Rd in Rockville. Health care services will be available for Ivy Tech students and community residents who wish to visit the mobile health center.</p>
		<p>The mobile health center is a mobile medical clinic that travels to a variety of locations in the Wabash Valley.  It serves the community by providing a full range of quality health care services and treatment. Supporting two private exam rooms, patients feel the same comfort of being at a medical family practice office. Services provided include, but are not limited to, immunizations, health screenings, physicals, well-child checks, cold/fever and sore throat.</p>
		<p>The mobile health center accepts Medicaid, Medicare and commercial insurance. Patient&rsquo;s insurance will be billed for any services the patient receives on the mobile health center. Additionally, patients may be eligible for sliding fee scale if they do not have insurance.</p>
		<p>&ldquo;We are pleased to be partnering with the Vermillion-Parke Community Health Center to bring their mobile health center to Rockville,&rdquo; said Rene Hankins, executive director of outreach at Ivy Tech.  &ldquo;The Center will be available and provide on-site access to health services for our students as well as the citizens of Rockville.&rdquo;</p>
		<p>The PCLC provides a wide variety of general education courses to about 300 students per semester. The Center was established in 2005 through an Indiana Division of Workforce Development grant to retrain displaced workers. The Parke County Community Foundation and Ivy Tech were awarded a grant from the Lilly Endowment to build a new facility in 2006. That facility also serves as a community center for groups and activities, hosts a popular continuing education series and houses a WorkOne Center. For more information about Ivy Tech and the PCLC, call (765) 569-1065 or visit  <a href="http://www.ivytech.edu/wabashvalley">here</a>.</p>
		<p>For questions or more information on the mobile health center, please contact any VPCHC location: Clinton (765) 828-1003, Cayuga (765) 492-9042, or Bloomingdale (765) 498-9000.</p>
