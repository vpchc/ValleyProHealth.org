  <h2>Health Center Announces Name Change</h2>
  <h3 id="page-tinytitle">February 17, 2014</h3>
  <div id="archive-news-section">
    <p>Vermillion-Parke Community Health Center, Inc., has announced the organization is changing its name to Valley Professionals Community  Health Center (VPCHC).  The name change reflects the organization&rsquo;s growth and opportunities to serve the surrounding communities.</p>
    <p>As a leading local health care organization, VPCHC provides both primary medical care  and behavioral health care services. Since first opening in Clinton in 2008, the health center has grown to three health center locations within Vermillion and Parke counties, including Clinton, Cayuga and Bloomingdale. VPCHC also operates a mobile health center that provides health care services to students at local schools and other community locations.</p>
    <p>In addition, VPCHC offers an after-hours clinic at the Clinton facility, Monday through Thursday, to provide available services so patients can avoid the emergency room for primary health care needs. All VPCHC locations accept Medicaid, Medicare, and commercial insurance. If an individual does not have any health care coverage, they may be eligible for a sliding fee scale, which is based on family size and income.</p>
    <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services.  VPCHC accepts Medicaid, Medicare, and commercial insurance.</p>
