  <h2>Screening Mammogram Funds Available at Health Center</h2>
  <h2 id="page-tinytitle">June 16, 2017</h2>
  <div id="archive-news-section">
    <p>Have you or someone you know, been diagnosed with breast cancer? Are you aware of the importance of early detection with breast cancer, which can increase a person's survival rate?</p>
    <p>Valley Professionals Community Health Center (VPCHC) has received funding through the Indiana Breast Cancer Awareness Trust to provide screening mammograms for women who are uninsured. If you are over 40 years old and are in need of a screening mammogram, call our office to schedule an appointment with one of our medical providers to get a clinical breast exam and to obtain a voucher for a free mammogram at Union Hospital Clinton. Women considered high-risk under the age of 40 would also be eligible. Patients must qualify through the health centerâ€™s eligibility intake form, in order to obtain a voucher.</p>
    <p>Mammogram funding is made possible by a grant from the Indiana Breast Cancer Awareness Trust, Inc. through sales of the Indiana Breast Cancer license plate. Patients will be financially responsible for the medical provider visit. Valley Professionals offers an income-based sliding fee scale for underinsured and uninsured patients.</p>
    <p>The mission of the Indiana Breast Cancer Awareness Trust, Inc. (IBCAT) is to increase awareness and improve access to breast cancer screening and diagnosis throughout Indiana. Since the breast cancer awareness license plate was first made available in 2002, nearly $4 million has been distributed by IBCAT through competitively awarded grants to organizations throughout the state for innovative projects for screening mammography services not otherwise available to medically underserved populations in Indiana. Indiana residents may purchase an Indiana Breast Cancer Awareness license plate by visiting their local Bureau of Motor Vehicles branch or the BMV website at <a href="https://www.in.gov/bmv">www.in.gov/bmv</a>.</p>
    <p>According to Susan G. Komen for the Cure, in 2017 it is estimated that 252,710 new cases of invasive breast cancer will be diagnosed among women. Valley Professionals would like to share a reminder of how important early detection is with breast cancer. When breast cancer is detected early, and in the localized stage, the five-year survival rate is 100%, according to the National Breast Cancer Association. Through monthly breast exams and yearly mammogram screenings, cancer can be found in its early and most curable stage. All women over 40 should receive a screening mammogram and a Clinical Breast Exam (CBE) yearly, by a medical provider.</p>
    <p>Contact Valley Professionals to schedule a clinical breast exam and obtain a free mammogram voucher for qualifying patients at (765) 828-1003.</p>
    <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services. VPCHC accepts Medicaid, Medicare, and commercial insurance.</p>
