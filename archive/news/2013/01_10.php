  <h2>Medical Officer Joins Health Center</h2>
  <h3 id="page-tinytitle">January 10, 2013</h3>
  <div id="archive-news-section">
    <p>The Vermillion-Parke Community Health Center would like to welcome its new Chief Medical Officer, Dr. Eric D. Beachy. </p>
    <p>Dr. Beachy graduated from the Indiana University School of Medicine and completed his undergraduate studies at Bethel College in Mishawaka, IN.</p>
    <p>Dr. Beachy completed his medical residency training through Union Hospital Family Medicine Residency Program in Terre Haute in 2001. He is board certified through the American Board of Family Medicine and is a member of the American Academy of Family Physicians and the Christian Medical/Dental Association.</p>
    <p>Dr. Beachy has worked in a variety of clinical settings, including practicing as a rural family physician in northern Indiana, serving as a member of the faculty of the Union Hospital Family Medicine Residency Program and also as medical director of both the Clay City Center for Family Medicine and most recently the VA Community Based Outpatient Clinic in Terre Haute.  Those experiences provided him the opportunities to care for many patients. Dr. Beachy says he enjoys practicing family medicine because, &ldquo;it allows me to really get to know patients and families and to work collaboratively with them to meet their health needs.&rdquo;</p>
    <p>In addition to practicing medicine, Dr. Beachy enjoys spending time with his wife and five children and caring for animals, including 19 chickens, 10 goats, two steers, a horse and two cats.</p>
    <p>Dr. Beachy will be serving patients at the VPCHC Bloomingdale health center. The Bloomingdale health center is open Monday through Friday, from 8:00 a.m. to 5:00 p.m. and is located at 201 W. Academy St., Bloomingdale, Ind. If you need assistance or would like to schedule an appointment, please call the Bloomingdale health center at (765) 498-9000.</p>
