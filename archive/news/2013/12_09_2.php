  <h1>News Archive</h1>
  <h2>Inter-Professional Education Program for Local Medical Students</h2>
  <h3 id="page-tinytitle">December 9, 2013</h3>
  <div id="archive-news-section">
    <p>Vermillion-Parke Community Health Center (VPCHC) recently hosted a student pilot program in partnership with the Rural Health Innovation Collaborative (RHIC), at its Bloomingdale health center. The program focused on the concept of inter-professional education, with a group of local medical students led by VPCHC Chief Medical Officer, Dr. Eric Beachy.</p>
    <p>The program is designed to prepare students from various health care settings to collaborate and work effectively as an inter-professional team to promote the best health care outcomes for patients.</p>
    <p>&ldquo;Traditionally, health care professionals have been educated in silos, with very little interaction with students in other health care settings.  An example would be mental health and physician education, where it is obvious that the patient&rsquo;s best interests are served by the two healthcare areas working together,&rdquo; stated Dr. James Buechler, a RHIC board member.  &ldquo;This project is part of the early stages of a national movement to better prepare our health care workforce for collaborative practice.&rdquo;</p>
    <p>During this program, students met with patients separately or in a group and later reviewed to report and discuss their observations. Students also met as a group to discuss patient visits and the unique role of each health care area in the management of patient care.</p>
    <p>Upon completing this program, students gain an improved understanding of Federally Qualified Health Centers, enhance skill sets such as history taking and physical examination, become more comfortable communicating with students in other health care settings, further recognize the level of education, training and skill sets required of other health care areas, and better understand the benefits of inter-professional education and the collaboration of multiple health care areas, toward improving patient care.</p>
    <p>Participating students for this project included a medical student from Indiana University, a physician&rsquo;s assistant from Indiana State University, and a behavioral health student from Indiana State University. Other healthcare areas that are targeted to receive this training in the future include respiratory therapy, pharmacy, nursing, nurse practitioners, nutrition and social work.The concept of inter-professional education is a guiding principle of the RHIC in its mission to better prepare future health care providers. The mission of the RHIC is to improve and expand inter-professional education, training, and deployment of future health care providers, especially for those committed to serving rural populations.</p>
    <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC offers primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
