  <h2>Family Nurse Practitioners Join Local Health Center</h2>
  <h3 id="page-tinytitle">July 16, 2012</h3>
  <div id="archive-news-section">
    <p>The Vermillion-Parke Community Health Center would like to welcome Family Nurse Practitioners, Nicole Hall, FNP-C-C, and Jennifer Inman, FNP-C-C.</p>
    <p>Nicole Hall is a 2011 graduate of Indiana State University Family Nurse Practitioner Program.  In 2007, she completed her Undergraduate studies in Nursing at Indiana State University in Terre Haute.</p>
    <p>Prior to becoming a Family Nurse Practitioner, Nicole worked at the Wound Center at Union Hospital Terre Haute, St. Vincent Clay Hospital Emergency Department, and Harsha Behavioral Center. She currently teaches in the Baccalaureate Program at Indiana State University and serves in the Indiana Air National Guard at Hulman Air Field.</p>
    <p>Jennifer Inman is a 2011 graduate of Indiana State University Family Nurse Practitioner Program. In 2008, she completed her undergraduate studies in Nursing at Indiana State University in Terre Haute.</p>
    <p>Jennifer has worked in a variety of clinical settings, including Endocrinology Department at Providence Medical Group, Endocrinology, OB/GYN, and Pulmonology at UAP Clinic and Emergency Department at Union Hospital.  She has participated in several volunteer activities with Lighthouse Mission, March for Babies, Salvation Army and various community health fairs. Jennifer will be the primary medical provider on the VPCHC Mobile School-Based Health Center, beginning in August 2012.</p>
    <p>Nicole and Jennifer are excited to join the VPCHC team and look forward to serving the community and providing the best carepossible!</p>
    <p>To schedule an appointment, contact the VPCHC office in Clinton or Cayuga. The health center is open Monday through Friday, from 8:00 a.m. to 5:00 p.m.  The After-Hours Clinic is open Monday through Thursday, from 5:00 p.m. to 8:00 p.m,, at the Clinton facility.</p>
