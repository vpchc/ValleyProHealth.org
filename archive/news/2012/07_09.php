  <h1>News Archive</h1>
  <h2>Health Center Receives New Access Point Grant</h2>
  <h3 id="page-tinytitle">July 9, 2012 </h3>
  <div id="archive-news-section">
    <p>Vermillion-Parke Community Health Center (VPCHC) is one of three health centers in Indiana to recently be awarded a New Access Point grant by Health and Human Services (HHS) Secretary Kathleen Sebelius.  In June 2012, Seblius announced awards of new grants made possible by the health care law to expand community health centers. </p>
    <p>The New Access Point funding is a $650,000 grant that VPCHC will receive annually, during the grant cycle. The grant will allow VPCHC to provide a health care facility in Bloomingdale, IN, and create at least five new jobs, while that number is expected to grow.   In Indiana, a total of $2,200,000 was awarded to the three health centers to help expand access to care for 9,600 additional patients.</p>
    <p>&ldquo;The health care law is making our community health centers stronger and ensuring more Americans get the care they need,&rdquo; said Secretary Sebelius. Community health centers work to improve the health of the nation by ensuring access to quality primary health care services. The awards announced in June total $128.6million and will go to community health centers in 41 States, the District of Columbia, Puerto Rico and the Northern Mariana Islands.</p>
    <p>As community-based and patient-directed organizations, health centers are well positioned to be responsive to the specific health care needs of their community. Through the Affordable Care Act&rsquo;s commitment to expand access to high quality health care for all Americans, these grants will support establishment of new full-time service delivery sites.</p>
    <p>&ldquo;Receiving this grant rewards the hard work of the citizens of Bloomingdale and the board and staff of the health center.&nbsp; We are extremely excited to provide access to primary care in an area of Parke County that currently does not have a healthcare facility,&rdquo; said Elizabeth Burrows, CEO of VPCHC.&nbsp;</p>
    <p>Health centers are also an integral source of local employment and economic growth in many underserved and low-income communities. Since opening in February 2008, VPCHC has created 41 new jobs and looks forward to providing additional employment opportunities through the Bloomingdale health center. The recently announced grant awards will infuse critical dollars into health centers nationwide and their surrounding communities, enhancing health centers&rsquo; ability to serve more patients and creating thousands of jobs across the country.</p>
    <p>&ldquo;The board of VPCHC is excited about the opportunities of expanding services into the&nbsp;Bloomingdale area, with ability to deliver care to citizens of the surrounding areas.&nbsp; This clinic site will serve to provide quality health care at the back door of&nbsp;many rural residents and provide a medical home for them,&rdquo; said Becky Holbert, VPCHC Board President.</p>
    <p>VPCHC will distribute additional details about the opening of the Bloomingdale health center, once information becomes further available. The facility is expected to be open later this year.</p>
    <p>The Health Resources and Services Administration is part of the U.S. Department of Health and Human Services. HRSA is the primary Federal agency responsible for improving access to health care services for people who are uninsured, isolated, or medically vulnerable. For more information about HRSA and its programs, visit www.hrsa.gov.</p>
    <p>Health Center New Access Point grants, listed by organization and state, are available <a href="http://www.hrsa.gov/about/news/2012tables/120620nap.php">here</a>.</p>
