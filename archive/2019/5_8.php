  <h6>Crawfordsville Site Relocation</h6>
  <h6>May 8th, 2019</h6>
  <div class="archive-section" style="width: 1100px; margin: auto;">
Valley Professionals Community Health Center is increasing its ability to provide quality care for individuals and families in Montgomery County. The Crawfordsville health center has relocated to a larger facility at 1702 Lafayette Road, increasing additional services and access to care for patients.

Available services include primary care, behavioral health, dental, and patient resource services. Dental services were recently added to the health center in March, with full-service care available on Thursdays. The health center accepts Medicaid, Medicare, commercial insurance, self-pay, and offers a sliding fee scale based on family size and income for individuals and families without insurance.

Medical providers at the Crawfordsville location include Dr. Patrick Foley, MD, Jessica Bekkering, Family Nurse Practitioner, and Dr. Anastasia Yi, DO. Dr. Yi recently joined the Valley Professionals team and is excited to be providing care in her hometown. Dr. Yi’s holistic approach to healthcare includes osteopathic manipulation. She is also passionate about women’s health, provides preventative exams and multiple family planning options including Nexplanon. Dr. Yi is bilingual in English and Spanish.

Behavioral health providers include Psychiatrist Dr. Paul Taraska, MD; Psychiatric Mental Health Nurse Practitioner, Jason Heck, Licensed Clinical Social Workers Misha Bennett and Keith Seegers, and Dana Tinkle, a Licensed Mental Health Counselor and Registered Play Therapist.

The Crawfordsville health center is open Monday through Friday, from 8:00 a.m. to 5:00 p.m., with extended hours available by appointment only on Thursdays until 8:00 p.m. Dental services are available on Thursdays from 8:00 a.m. to 5:00 p.m. For more information or to schedule an appointment, please call (765) 362-5100

or visit www.valleyprohealth.org.

Valley Professionals Community Health Center is a federally qualified health center (FQHC) that provides comprehensive and integrated health care for all individuals and families, while promoting health education opportunities for the community, students, and health care professionals. Valley Professionals currently serves the Wabash Valley and surrounding areas with locations in Bloomingdale, Clinton, Cayuga, Crawfordsville, Rockville and Terre Haute.
