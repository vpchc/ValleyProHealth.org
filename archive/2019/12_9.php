  <h6>Health Center Awarded Challenge Grant from Community Foundation</h6>
  <h6>December 9th, 2019</h6>
  <div class="archive-section" style="width: 1100px; margin: auto;">
Valley Professionals Community Health Center (VPCHC) was recently awarded a $50,000 challenge grant through the Wabash Valley Community Foundation in October 2019 that will support funding of the health center’s project to bring primary, behavioral health and pharmacy services to West Terre Haute. The Community Foundation will provide $1 for every $2 raised by the health center, up to a total match of $50,000. <br /> <br />

VPCHC has launched its plans to establish the delivery of health care services and reduce barriers of care to the town of West Terre Haute through a new health center clinic. As a federally qualified community health center, VPCHC recognizes the need for individuals to have appropriate access and availability to health care and related services, as the center’s mission is dedicated to providing comprehensive and integrated health care for all individuals and families. <br /> <br />

“The health outcomes of West Terre Haute and surrounding areas are underserved with very limited access to options supporting positive individual and community health, prevention and well-being,” states Chief Executive Officer Terry Warren. “Challenges of poverty, unemployment, and uninsured rates poses substantial barriers in obtaining health care services. The investment of a clinic will provide affordable, long-lasting and positive transformation within the community, supporting the overall health of its individuals and families.” <br /> <br />

The delivery of integrated health care services will improve positive health outcomes, allowing prevention of illnesses and chronic diseases, as well as health management and behavioral services for whole-person care. VPCHC believes the investment in West Terre Haute will strengthen the community’s development, longevity and health of its residents, demonstrating a proactive focus on prevention within the community. Integrated services aim to improve positive outcomes and dissolve barriers in the community such as transportation, access, affordability and additional resources. <br /> <br />

VPCHC’s project evolved from a collaborative community effort through compassionate, community-driven and engaged individuals with a common goal of improving quality of life for local residents. Property has been obtained at the corner of National Avenue and South 6th Street through a generous donation from Patricia Minnis. <br /> <br />

Implementation of this project will include facility construction to house services. VPCHC will contract with architectural and engineering services for other aspects of the project.   <br /> <br />

A Go Fund Me page has been made available to accept donations for those interested in supporting this project at www.gofundme.com/f/westterrehauteclinic. <br /> <br />

Valley Professionals Community Health Center, Inc. was established as a funded Section 330 Community Health Center in 2007. VPCHC is dedicated to providing comprehensive primary health care and related services for all individuals and families, and provides primary care, behavioral health, dental and resource services. The health center has clinics located in Montgomery, Parke, Vermillion and Vigo counties.  Medicaid, Medicare, marketplace and commercial insurance, sliding fee scale and self-pay accepted. For more information, visit www.valleyprohealth.org.




