  <h6>Groundbreaking Set for Health Center Expansion</h6>
  <h6>February 18, 2019</h6>
  <div class="archive-section" style="width: 1100px; margin: auto;">
Valley Professionals Community Health Center is hosting a groundbreaking event on Thursday, February 21st at 10:00 a.m. in celebration of the clinic's expansion at the Clinton facility in Vermillion County, located at 777 S. Main Street. The public is welcome to attend.<br /><br />Valley Professionals was awarded $1 million in capital funding in May 2016 for facility renovation, expansion, or construction, and will utilize funds for expansion of its Clinton health center.   Funding for the award came from the Affordable Care Act's Community Health Center Fund, which was extended in the Medicare Access and CHIP Reauthorization Act of 2015.<br /><br />TJ QUOTE<br /><br />Expansion of the facility will include adding an additional 15,870 square feet to the building, comprised of main floor and second floor additions, from the building's current roughly 9,000 square feet.  The new space will include 16 additional exam rooms, group treatment room, behavioral health treatment rooms and administrative offices, creating approximately 22 new offices and workstation space. <br /><br />Valley Professionals is partnering with Halstead Architects for the design and oversight of the construction of the health center expansion project. Halstead Architects specializes in community centers and community-focused architecture. The project contract was recently awarded to Garmong Construction Services of Terre Haute, one of the local leading construction management companies in Indiana.<br /><br />Valley Professionals Community Health Center is a Federally Qualified Health Center dedicated to providing comprehensive primary health care and related services for all individuals and families. Valley Professionals provides primary care, behavioral health, dental, and resource services.  For more information, visit www.valleyprohealth.org.<br /><br />This project is supported by the Health Resources and Services Administration (HRSA) of the U.S. Department of Health and Human Services (HHS) under grant number C8DCS29613 Health Infrastructure Investment Program for $1 million, with 60% of the overall project financed with non-governmental sources. This information or content and conclusions are those of the author and should not be construed as the official position or policy of, nor should any endorsements be inferred by HRSA, HHS or the U.S. Government.<br /><br />
