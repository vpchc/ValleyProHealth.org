  <h6>Sunset on Summer - Free Community Movie Celebrations!</h6>
  <h6>July 17th, 2019</h6>
  <div class="archive-section" style="width: 1100px; margin: auto;">
Valley Professionals Community Health Center is celebrating National Health Center Week during July 29 through August 1 by providing a free outdoor movie night, open to the public, in the communities and counties served by the health center.<br /><br /> 

As National Health Center Week 2019 is officially celebrated August 4-10, Valley Professionals looks forward to providing an end of summer celebration that can be enjoyed by our communities in effort to say thank you and show our appreciation and gratitude to all those we serve, including patients, local community members, organizations and businesses for their continued support. This year’s National Health Center Week theme is “Rooted in Communities” and is designated to celebrate America’s Health Centers and the local services they provide.<br /><br />

National Health Center Week is celebrated annually in August as part of the national observance of celebrating the success of America’s health centers and raising awareness about the mission and accomplishments of health centers as local solutions for affordable and accessible health care. Health centers serve more than 27 million people nationwide and are increasingly becoming the trusted provider of choice for many families. Evidence shows patients choose health centers because they are convenient, affordable, and offer a range of services from a team of caring professionals.<br /><br /> 

While there are countless reasons to celebrate health centers, among the most important and unique is their long success in providing access to affordable, high quality, cost-effective health care to medically vulnerable and underserved people throughout the United States. With health center locations in Clinton, Cayuga, Bloomingdale, Crawfordsville, Rockville and Terre Haute, Valley Professionals continues to provide access to quality health care services and address medical needs of the surrounding
areas.<br /><br />

“Every day I witness the value our health centers have on our patients, by providing a patient-centered health care home for all,” said T.J. Warren, Valley Professionals CEO. “When patients have a primary care provider they can trust, they are more likely to stay committed to their care, which in turn provides a healthier lifestyle. We provide a range of services on site, including primary care services, behavioral health, and dental services. Our patients not only get the care they need under one roof, but they are treated as individuals, with dignity and respect. This is what health care should be, and what we celebrate during National Health Center Week.” <br /><br />

In addition to a free movie, free popcorn and bottled water will also be available during the movie celebration events.  Thanks to our business sponsors who are supporting these special events at no cost to community members: Joink, Anthem, Charlie’s Pub & Grub, Chad’s Communication Solutions, AssuredPartners, Halstead Architects, Azara Healthcare, Vermillion County Economic Development Council, Clinton Breakfast Optimist Club, Union Hospital Clinton, and JR Pharmacy. View the Vallely Professionals Facebook page for more event details and information.<br /><br />

Valley Professionals is a Federally Qualified Health Center that provides comprehensive integrated healthcare for all individuals, while promoting educational opportunities. Visit www.valleyprohealth.org for more information.<br /><br /> 

To learn more about the mission and accomplishments of Community Health Centers, visit the National Association of Community Health Centers at www.nachc.org or www.healthcenterweek.org


