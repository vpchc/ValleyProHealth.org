	<h5>News Article</h5>
  <h6>2016 Summer Block Party</h6>
  <h6>July 25, 2016</h6>
  <div class="archive-section">
    <p>Valley Professionals Community Health Center (VPCHC) is celebrating one last opportunity of summer fun before students head back to school, by hosting their Second Annual Summer Block Party on Friday, August 5th at Sportland Park in Clinton, from 5:00 p.m. to 8:00 p.m. The block party is being held in honor of National Health Center Week and is open to the public.</p>
    <p>The VPCHC Summer Block Party will feature many activities and fun for all, including, performances by local favorite The Lemon Brothers band, Tumble Mania, and magician Jason Brewer. The event is a perfect opportunity for free family fun, with local community exhibitors, health education and screenings, fun zone, food, and more! Guests are encouraged to bring lawn chairs for the concert.</p>
    <p>Valley Professionals is hosting this event to show thanks and appreciation for their patients, local community members, organizations, and businesses for their continued support, and show the importance of available primary, behavioral health and dental care services they are able to provide in a rural population.</p>
    <p>National Health Center Week is celebrated annually, during the second week of August, as part of the national observance of celebrating the success of America’s health centers, and raising awareness about the mission and accomplishments of health centers as local solutions for affordable and accessible health care. Health centers serve more than 22 million people nationwide, and are increasingly becoming the trusted provider of choice for many families. Evidence shows patients choose health centers because they are convenient, affordable, and offer a range of services from a team of caring professionals.</p>
    <p>“Every day I witness the value our health centers have on our patients, by providing a patient-centered health care home for all,” said T.J. Warren, Valley Professionals CEO. “When patients have a primary care provider they can trust, they are more likely to stay committed to their care, which in turn provides a healthier lifestyle. We provide a range of services on site, including primary care services, behavioral health, and dental services. Our patients not only get the care they need under one roof, but they are treated as individuals, with dignity and respect. This is what health care should be, and what we celebrate during National Health Center Week.”</p>
