  <h6>Substance Abuse Funding to Aid Local Fight against Opioid Epidemic</h6>
  <h6>March 16, 2016</h6>
  <div class="archive-section">
    <p>Valley Professionals Community Health Center (VPCHC) was recently awarded $406,250 in funding aimed at improving and expanding the delivery of substance abuse services, with focus on treatment of opioid use disorders in underserved populations, through the Affordable Care Act.  Valley Professionals will utilize funds by establishing a medication-assisted treatment (MAT) program to help combat the local challenges of opioid abuse and addiction, serving individuals in the health center's service area of Montgomery, Parke, Vermillion and Vigo counties.</p>
    <p>According to a news release from the Department of Health and Human Services (HHS), HHS Secretary Sylvia M. Burwell announced on March 11, that approximately $94 million in Affordable Care Act funding was awarded to 271 health center sites in 45 states, the District of Columbia, and Puerto Rico, to expand delivery of substance abuse services in health centers. Valley Professionals was one of four Indiana health care facilities to receive funding, which totals to $1.5 million statewide.</p>
    <p>HHS also reported that the abuse of and addiction to opioids, such as heroin and prescription pain medication, is a serious and increasing public health problem, and these awards administered by the HHS Health Resources and Services Administration (HRSA), will increase the number of patients screened for substance use disorders, increase the number of patients with access to MAT for opioid use and other substance use disorder treatment, and provide training and educational resources to help health professionals make informed prescribing decisions, and treat nearly 124,000 new patients.</p>
    <p>&ldquo;Valley Professionals is excited to offer a comprehensive treatment option for our patients that will provide a caring and respectful atmosphere for their recovery process,&rdquo; stated Dr. Julia Wernz, Director of Behavioral Health at Valley Professionals. &ldquo;These funds will greatly influence VPCHC's ability to provide exemplary care to those suffering from addiction.&rdquo;</p>
    <p>Valley Professionals is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive integrated health care for all individuals, while promoting educational opportunities. Valley Professionals provides primary, behavioral health, dental and other support services to the citizens of Montgomery, Parke, Vermillion and Vigo counties, and surrounding communities.</p>
    <p>To view the full Health and Human Services news release, click <a> http://www.hhs.gov/about/news/2016/03/11/hhs-awards-94-million-to-health-centers.php>here</a>.</p>
