  <h6>Dr. Waltz Receives Teacher of the Year Award</h6>
  <h6>October 7, 2016</h6>
  <div class="archive-section">
    <p>Local family practice physician and new Chief Medical Officer for Valley Professionals Community Health Center, Dr. Steven G. Waltz, was awarded Teacher of the Year by Union Hospital’s Family Residency Program. The recipient of this award is chosen by the graduating class of physicians based on who they feel contributed the most to their education and training throughout their three-year residency.</p>
    <p>Dr. Waltz is a 1987 graduate of Union Hospital’s Family Medicine Residency, and has been practicing in the Wabash Valley for over thirty years.  It was around ten years ago that he decided to give up his afternoon off to begin teaching the next generation of physicians.  Waltz is honored to have received this award acknowledging the efforts of teachers to help pass patient care skills on to the next generation of physicians who will sustain the profession, “Teaching involves some degree of sacrifice, in terms of time, but is necessary and valuable to society as a whole—it’s nice to see that young professionals appreciate and value it.”  He realizes that a good teacher must set the bar for his students, “In teaching, while you try to be complimentary and positive, you also have to enforce standards in quality and behavior, and while that may not always be enjoyable, it is essential for development into compassionate, competent professionals.  It works both ways.  Their energy, excitement, and fresh thinking helps me improve as well.”</p>
    <p>For Dr. Waltz, his expertise not only benefits budding physicians, but also established physicians, nurse practitioners, nurses, and medical students as he begins his career as Chief Medical Officer with Valley Professionals Community Health Center.  Dr. Waltz recently joined the Valley Professionals team this past summer. He hopes to help sustain an organization of medical providers to meet the primary care needs of the west central Indiana population in a competent, caring, and efficient way, “While teaching, I worked with Valley Professionals and realized there was an opportunity to utilize my clinical experience of thirty years to help providers here in their efforts to provide the best care,” says Waltz.</p>
