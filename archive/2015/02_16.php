  <h5>News Archive</h5>
  <h6>Nurse Practitioner Joins Health Center</h6>
  <h6>February 16, 2015</h6>
  <div class="archive-section">
    <p>Valley Professionals Community Health Center (VPCHC) is pleased to announce the addition of Family Nurse Practitioner, Tammy Mundy, to its medical staff. Mundy joined VPCHC in late 2014.</p>
    <p>A graduate of Indiana State University, Mundy holds a Master of Science in Nursing degree through completion of Indiana State&rsquo;s Family Nurse Practitioner program. In addition, she also earned her bachelor&rsquo;s degree in nursing at Indiana State.</p>
    <p>Mundy has worked in a variety of clinical settings, where she has gained experience and opportunities to provide care to many patients.&nbsp; Prior to joining VPCHC, her clinical experience includes working as a registered nurse on various units such as medical-surgical, oncology, and intensive care, as well as a university adjunct nursing instructor, and as a nurse practitioner in family practice clinics and occupational medicine.</p>
    <p>Mundy enjoys practicing family medicine because of the variety of illnesses that she is able to treat.&nbsp; &ldquo;I enjoy seeing patients of all ages.&nbsp; It is rewarding to build a partnership with a patient and family that can last through the years.&nbsp; My passion has always been to help people in need,&rdquo; stated Mundy. </p>
    <p>A member of Sigma Theta Tau International, Mundy is also certified in Basic Life Support (BLS) through the American Heart Association, and certified through the American Nurses Credentialing Center. In addition to practicing medicine, she enjoys reading and visiting antique shops.</p>
    <p>Mundy will be seeing patients primarily at the VPCHC Clinton location, but will occasionally see patients at the Bloomingdale and Cayuga sites.&nbsp; She is excited to be a part the VPCHC team and enjoys serving the community and providing patient care to new and current patients.</p>
    <p>The Clinton health center is open Monday - Friday from 8:00 a.m. to 5:00 p.m. After-Hours Clinic services are available Monday &ndash; Thursday, from 5:00 p.m. to 8:00 p.m. If you need assistance or would like to schedule an appointment, please call the Clinton office at (765) 828-1003. For more information, visit www.vpchc.org</p>
    <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive integrated health care for all individuals while promoting educational opportunities. VPCHC provides primary care, behavioral health care, and dental services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
