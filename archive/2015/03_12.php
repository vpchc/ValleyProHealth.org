  <h5>News Archive</h5>
  <h6>Nurse Practitioner job posting - Crawfordsville</h6>
  <h6>March 12, 2015</h6>
  <div class="archive-section">
    <p>Valley Professionals Community Health Center (VPCHC) is a Federally Qualified Health Center (FQHC) with locations in Clinton, Cayuga, and Bloomingdale, Indiana, as well as a Mobile School-Based Health Center. &nbsp;In addition, VPCHC is excited to announce that it is partnering with Dr. Hwang in Crawfordsville to provide care to his current patients, as well as provide expanded services to the Crawfordsville community.&nbsp; As such, VPCHC is looking for a nurse practitioner to join its outstanding and compassionate team in the Crawfordsville office.&nbsp;&nbsp;</p>
    <p>Dr. Hwang has been practicing in Crawfordsville as a family practice physician for almost 38 years.&nbsp; In the summer of 2015, Dr. Hwang will be joining the VPCHC team to expand on his growing practice.&nbsp; VPCHC and Dr. Hwang are looking to hire a nurse practitioner to assist in these expansion efforts.&nbsp;</p>
    <p>VPCHC has a strongly integrated Behavioral Health program at each of its locations, which will also be integrated into the Crawfordsville practice. &nbsp;VPCHC Behavioral Health providers are present at each VPCHC location.&nbsp; The behavioral health providers work closely with the medical providers to treat not only the physical well-being of the patient, but the emotional well-being as well.&nbsp;</p>
    <p>VPCHC strives to provide quality care to all patients.&nbsp; VPCHC&rsquo;s Clinton, Cayuga, and Bloomingdale locations have all been recognized as a Level 3 Patient Centered Medical Home, a distinction awarded only to facilities which have demonstrated a significant and sustained commitment to patient-focused care.&nbsp;&nbsp; This impacts how VPCHC structures the clinicians&rsquo; days, encouraging a team-focused approach to care delivery, including daily team &ldquo;huddles,&rdquo; to ensure the team is well prepared to meet the needs of patients each day, as well as provide an emphasis on preventative health appointments to create enough margin in schedules so preventative health care needs can be addressed, even during acute visits if appropriate.&nbsp; Overall, the transition to PCMH has improved the providers&rsquo; satisfaction with the work environment.&nbsp;</p>
    <p>VPCHC places a premium on work-life balance with generous holiday and paid time off (PTO) allowances.&nbsp;&nbsp; A competitive salary and benefits package is offered.&nbsp;&nbsp; Continuing Medical Education (CME) is encouraged through monetary and time allowances (in addition to PTO).&nbsp; Several current clinicians have successfully qualified for loan repayment through the National Health Service Corps.</p>
    <p>VPCHC&rsquo;s mission is to serve the health care needs of its communities, particularly the underserved portions of the population in the mostly rural service area.&nbsp;&nbsp; The ideal candidate for this position will have a passion for caring for underserved populations.&nbsp; Please contact Tiffani, VPCHC Director of Human Resources, for more information.</p>
