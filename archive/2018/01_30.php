  <h6>Health Center Celebrates 10 Years of Serving Communities</h6>
  <h6>January 30, 2018</h6>
  <div class="archive-section">
    <p>Valley Professionals Community Health Center is celebrating ten years of growth, expanded services, increased employment and healthier communities in February 2018.</p>
    <p>Valley Professionals opened its first health center in February 2008, located in Clinton, primarily serving residents of Vermillion and Parke counties.  Since that time, the health center has grown to serve communities across four counties in Indiana and the surrounding areas. The health center operates six health center facilities located in Clinton, Cayuga, Bloomingdale, Rockville, Crawfordsville and Terre Haute, as well as a mobile health center.</p>
    <p>By visiting Valley Professionals, patients can receive integrated care services under one roof.  Services provided include primary health care, behavioral health, dental, and patient support services. Over 17,000 patients were served by the health center in 2017, with 72,000 patient visits overall. The growth of Valley Professionals has undoubtedly increased access to health care services, provided economic impact, as well as eliminating barriers for vulnerable populations by providing resources for needs such as transportation, health insurance, medication, and food.</p>
    <p>&ldquo;When I took over as CEO in 2015, my vision was and still is, to focus on the opportunities that exist in our community,&ldquo; stated T.J. Warren, CEO of Valley Professionals. &ldquo;With the help of a supportive board and dedicated staff, I feel we have made great progress towards this vision by creating many new jobs and recruiting very successful providers to our rural underserved areas.&ldquo;  </p>    
    <p>Since opening in 2008, staffed with only 11 employees, the health center has proved to be a strong employer for the local communities.  By 2013, the number of employees climbed to 52, including full-time and part-time positions.  To date, the health center employs 171 people across all of its clinics.</p>
    <p>The health center takes extreme pride in their accreditation through The National Association of Quality Assurance as a Patient-Centered Medical Home, which provides all-inclusive primary care that creates partnerships between patients and their family and the primary care provider and health care team. This format of patient care allows the health center to work hand in hand with patients in generating better results of health and wellness, and further creating healthier individuals and communities.</p>
    <p>In addition to serving patients during weekday hours, growth and expansion at the health center has included an After-Hours Clinic that is available Monday through Thursday from 5:00 p.m. to 8:00 p.m., as well as the Saturday Clinic, open every Saturday from 9:00 a.m. â€“ 1:00 p.m.  Both services are located at the Clinton health center for acute care and are available for current and non-health center patients.</p>
    <p>Valley Professionals is thankful for continued support of our mission to create healthier communities in the Wabash Valley.</p>
