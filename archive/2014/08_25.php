	<h5>News Archive</h5>
	<h6>Nurse Practitioner Joins Health Center</h6>
	<h6>August 25, 2014</h6>
	<div class="archive-section">
		<p>Valley Professionals Community Health Center (VPCHC) is pleased to welcome Family Nurse Practitioner, Gretchen Blevins, FNP-C, to its medical staff.</p>
		<p>A graduate of Marshall University School of Nursing, located in Huntington, West Virginia, Blevins holds a Master of Science degree in nursing.  In addition, she earned her bachelor&rsquo;s degree in nursing from the University of New Hampshire.</p>
		<p>Blevins has worked in a variety of clinical settings located in Kentucky and New Hampshire, where she has gained experience and opportunities to provide care to many patients.  Prior to joining VPCHC, her clinical experience includes working as an operating room and cardiovascular operating room RN, clinical research coordinator, clinical leader for a cardiac interventional unit, and a hospital nursing supervisor.</p>
		<p>A member of the American Association of Nurse Practitioners and American Nurses Association, Blevins is certified in advanced cardiac life support and pediatric advanced life support.</p>
		<p>&ldquo;I enjoy working in family practice because it makes my work a more personal and meaningful experience when I am able to help individuals and whole families in the community that I live in,&rdquo; stated Blevins.</p>
		<p>In addition to practicing medicine, Blevins enjoys spending free time with her family swimming, kayaking, ATV riding, and trying new things like sky diving when she gets the chance. </p>
		<p>Originally from Fayette County in Indiana, Blevins moved out of state when she was younger. Now that has relocated back to Indiana and in the Wabash Valley area, she says that moving back to Indiana was like coming home. The area provides the hometown environment that she enjoys for her family, as well as for providing patient care.</p>
		<p>Blevins will be seeing patients at the VPCHC Clinton location.  She is excited to join the VPCHC team and looks forward to serving the community and providing patient care to new and current patients.</p>
		<p>The Clinton health center is open Monday - Friday, from 8:00 a.m. to 5:00 p.m. After-Hours Clinic services are available Monday &ndash; Thursday, from 5:00 p.m. to 8:00 p.m. If you need assistance or would like to schedule an appointment, please call the Clinton office at (765) 828-1003.</p>
		<p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive integrated health care for all individuals while promoting educational opportunities. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
