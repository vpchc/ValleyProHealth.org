	<h5>News Archive</h5>
	<h6>Infant Mortality Task Force Takes Charge</h6>
	<h6>April 8, 2014</h6>
	<div class="archive-section">
		<p>A local task force has been created to develop ways to reduce Indiana&rsquo;s infant mortality rate. The Rural Health Innovation Collaborative (RHIC) of Terre Haute has been selected by the National Leadership Academy for the Public&rsquo;s Health, to lead the West Central Indiana Infant Mortality Reduction Task Force. The RHIC is an inter-professional education initiative dedicated to enhancing educational opportunities for teams of future health care professionals, particularly those preparing to practice in rural areas</p>
		<p>With 7.7 deaths per 1,000 live births, Indiana had the nation&rsquo;s sixth highest infant mortality rate in 2011. The task force will be known as &ldquo;Wabash Valley Healthy Moms and Babies,&rdquo; as it works to help more babies reach their first birthday.</p>
		<p>Valley Professionals Community Health Center (VPCHC) is a committed member of the task force, as CEO Elizabeth Burrows, joins Stephanie Laws, executive director of the RHIC, and Jack Turman, dean of Indiana State University&rsquo;s College of Nursing, Health, and Human Services, as the group leads this effort.</p>
		<p>The task force will focus on an eight-county area around Terre Haute, but efforts will have a statewide impact. Infant death rates in six of the counties (Clay, Greene, Owen, Parke, Putnam and Vermillion) exceed the state rate. Vigo and Sullivan counties have rates below the state average.</p>
		<p>As a local community health center, VPCHC provides patient education to mothers, parents and soon-to-be mothers/parents. VPCHC medical providers educate patients through counseling and handouts with information including breast feeding, sleeping, behavior, safety, and other topics.  Medical providers have multiple visits throughout the child&rsquo;s first year, for various well-child checks and immunization schedule. A variety of resources and services are available for parents with babies, including VPCHC&rsquo;s after-hours clinic, counseling services, and on-call provider during non-business hours.</p>
		<p>The National Leadership Academy for the Public&rsquo;s Health is funded by the Centers for Disease Control and Prevention.  The program is implemented by the Center for Health Leadership and Practice, and will provide training and support for one year, focusing on two tracks: development of leadership skills and team-based collaborative work aimed at effecting changes in policies and systems.</p>
		<p>The infant mortality task force is one of 29 teams from around the country chosen to participate in the third year of the National Health Leadership Academy.  It is the first Indiana team to be selected for inclusion in the three-year-old program.</p>
		<p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services.  VPCHC accepts Medicaid, Medicare, and commercial insurance.</p>
