	<h5>News Archive</h5>
	<h6>Physician Assistant Joins Health Center</h6>
	<h6>April 21, 2014</h6>
	<div class="archive-section">
		<p>Valley Professionals Community Health Center (VPCHC) is pleased to welcome Physician Assistant, Pamela Wall, PA-C, to its medical staff.</p>
		<p>Pamela, an Indianapolis resident, is a licensed Physician Assistant with over 15 years of experience supporting patient care across a variety of areas. She is a graduate of Butler University, where she earned her Physician Assistant degree, and completed her undergraduate studies at Indiana University &ndash; Indianapolis.</p>
		<p>Pamela has worked in a variety of clinical settings, where she has gained experience and opportunities to provide care to many patients.  Her clinical experience includes practicing as a Physician Assistant/Clinical Laboratory Supervisor at the Marion County Health Department in Indianapolis, as a Physician Assistant at Harris Health Care Associates in Suwanee, Georgia, and Mobile Doctors in Indianapolis. </p>
		<p>In addition to practicing medicine, Pamela enjoys working out at her local health club, hiking Indiana's beautiful state parks,&nbsp;sewing, traveling, and visiting her family in Indiana and Kentucky.</p>
		<p>Pamela will be seeing patients at the VPCHC Clinton health center. She is looking forward to working as a family practice Physician Assistant&nbsp;in a small town setting, as she sees this patient population as returning to her roots from a small southern Indiana town. Pamela is excited to join the VPCHC team and looks forward to serving the community and providing patient care.</p>
		<p>The Clinton health center is open Monday - Friday, from 8:00 a.m. to 5:00 p.m., and offers After-Hours Clinic services Monday &ndash; Thursday, from 5:00 p.m. to 8:00 p.m. If you need assistance or would like to schedule an appointment, please call the Clinton office at (765) 828-1003.</p>
		<p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive integrated health care for all individuals while promoting educational opportunities. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
