	<h5>News Archive</h5>
	<h6>Nurse Practitioner Joins Health Center</h6>
	<h6>March 10, 2014</h6>
	<div class="archive-section">
		<p>Valley Professionals Community Health Center (VPCHC) is pleased to welcome Family Nurse Practitioner, Nicole Cook, FNP-C, to its medical staff.</p>
		<p>Nicole is a graduate of St. Louis University School of Nursing, where she earned her Master of Science in Nursing, with specialization as Family Nurse Practitioner. She completed her undergraduate studies in nursing at Southern Illinois University &ndash; Edwardsville.</p>
		<p>Nicole has worked in a variety of clinical settings, where she has gained experience and opportunities to provide care to many patients.  Her clinical experience includes practicing as a nurse practitioner at Providence Medical Group, as an RN at Union Hospital, an RN at St. Mary&rsquo;s Health Center, Cardinal Glennon Children&rsquo;s Medical Center, and Missouri Baptist Medical Center, all located in St. Louis.</p>
		<p>Nicole enjoys working in family practice because of the variety of patients and illnesses she is able to care for. She enjoys working with children, especially in family practice, because she is able to watch them grow over the years.</p>
		<p>In addition to practicing medicine, Nicole likes spending time with family. Originally from the St. Louis area, Nicole frequently travels there to visit and enjoys attending sporting events, including Cardinal baseball games. </p>
		<p>Nicole will be seeing patients at the VPCHC Cayuga health center. She is excited to join the VPCHC team and looks forward to serving the community and providing patient care.</p>
		<p>The Cayuga health center is open Monday - Friday, from 8:00 a.m. to 5:00 p.m. If you need assistance or would like to schedule an appointment, please call the Cayuga office at (765) 492-9042.</p>
		<p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive integrated health care for all individuals while promoting educational opportunities. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
