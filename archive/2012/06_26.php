	<h5>News Archive</h5>
	<h6>Senator Lugar to Visit Mobile Health Center</h6>
	<h6>June 26, 2012</h6>
	<div class="archive-section">
		<p>The Vermillion-Parke Community Health Center (VPCHC) would like to announce Senator Richard G. Lugar will be visiting the VPCHC Mobile School-Based Health Center on July 6, for a dedication ceremony at South Vermillion High School.</p>
		<p>VPCHC is inviting citizens of Vermillion, Parke, and surrounding counties to please join them and Senator Lugar in celebrating the introduction of the Mobile School-Based Health Center.  The public is welcome to attend a dedication ceremony, hear remarks from Senator Lugar and partner organizations of the mobile health center and enjoy a tour of the mobile health center.</p>
		<p>The mobile health center will travel to all 14 schools in the five school corporations of Parke and Vermillion counties and provide a full range of health care services so students can avoid health-related absences and get additional support to succeed in the classroom.</p>
		<p>After four years on the Indianapolis Public Schools board and two terms as mayor, Richard G. Lugar was first elected to the U.S. Senate in 1976. He is the Republican leader of the Foreign Relations Committee and a member and former chairman of the Agriculture, Nutrition and Forestry Committee. Since his days as School Board member, Senator Lugar has worked toward a day where all children are able to have a healthy start in life, which includes access to health care and adequate nutrition and a strong public school system.&nbsp; He has been a long time champion of Community Health Centers, receiving a 2012 Distinguished Community Health Defender award by the National Association of Community Health Centers.</p>
		<p>In 2010, after reading about the success of mobile health units in Massachusetts, Senator Lugar asked staff to research the issue and to reach out to the Richard G. Lugar Center for Rural Health to gauge their interest.&nbsp; &ldquo;I am so pleased that an initial contact sparked an enthusiastic community response&nbsp;leading to the establishment of this wonderful mobile school-based health center, which will deliver needed health-related services to the 5,143 children that attend the five school corporations in Parke and Vermillion counties,&rdquo; said Lugar.</p>
		<p>The Mobile School-Based Health Center project was made possible through a School-Based Capital Program grant that was awarded to the health center in August 2011.  The School-Based Capital Program grant is made possible by the Affordable Care Act, which helps clinics expand and provide more health care services at schools nationwide. &ldquo;Receiving the grant for this Mobile School-Based Health Center will improve not only access to health care in our community, but will improve the overall health of our students.&nbsp; We are extremely excited to work with our local schools to serve the needs of their students,&rdquo; said Elizabeth Burrows, CEO of VPCHC.</p>
		<p>The dedication ceremony will take place on Friday, July 6th, at 2:00 p.m. at South Vermillion High School, located at 770 W. Wildcat Drive, Clinton, IN, 47842.</p>
