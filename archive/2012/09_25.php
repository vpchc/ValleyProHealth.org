	<h5>News Archive</h5>
	<h6>Groundbreaking Scheduled for New Health Center</h6>
	<h6>September 25, 2012</h6>
	<div class="archive-section">
		<p>Vermillion-Parke Community Health Center (VPCHC) is hosting a Groundbreaking Event on Friday, September 28th at 3:00 p.m., in celebration of a new health center facility that will be located at 200 W. Academy St. in Bloomingdale, Ind.</p>
		<p>VPCHC was one of three health centers in Indiana to be awarded a New Access Point grant by Health and Human Services (HHS) in June 2012.  The grant is made possible by the health care law to expand community health centers.  The New Access Point funding is a $650,000 grant that VPCHC will receive annually, during the grant cycle. The grant will allow VPCHC to provide a health care facility in Bloomingdale and create at least five new jobs, while that number is expected to grow.</p>
		<p>We are extremely excited to begin construction on the state-of-the-art health center.  Working with local partnerships in Parke County and beyond, we are able to make a significant investment in the town.  This health center will not only provide access to primary and behavioral health care but will create jobs for the community,&rdquo; said Elizabeth Burrows, CEO of VPCHC. </p>
		<p>VPCHC is partnering with Performance Services, Inc., for the design and construction of the Bloomingdale Community Health Center. Performance Services is an Indianapolis design-build engineering company that specializes in constructing and renovating educational and healthcare facilities to deliver optimal environments through both the Guaranteed Energy Savings Contract and Design-Build procurement methods.</p>
		<p>Performance Services is delighted to use our turnkey design and construction expertise to support Elizabeth Burrows and the Vermillion-Parke team with their third community health center project.&nbsp; In keeping with our Guiding Principles of service and stewardship, we are committed to delivering the highest quality building at the best value because we realize the importance and responsibility of providing health care to the rural community.&nbsp; We are excited to have been selected and look forward to working with the Bloomingdale community,&rdquo; said Jim Christie, healthcare business development manager for Performance Services.</p>
		<p>The Bloomingdale Community Health Center is expected to be open in Winter of 2013 and will comprise 3,000 square feet and include state of the art energy efficient features such as a multi-zone HVAC system, high quality insulation values in the roof and walls and energy efficient lighting. In addition, wireless access will be utilized throughout the facility.</p>
