	<h5>News Archive</h5>
	<h6>Open House for Renovated Health Center</h6>
	<h6>October 4, 2012</h6>
	<div class="archive-section">
		<p>The Vermillion-Parke Community Health Center (VPCHC) is hosting an Open House on Wednesday, October 10th, at the Clinton health center, in honor of its recent facility expansion and renovation.</p>
		<p>VPCHC is inviting residents of Vermillion and Parke counties, and surrounding communities, to please join them for this special occasion. Visitors are welcome to enjoy hors d&rsquo;oeuvres, hear from featured speakers Congressman Larry Buschon and Clinton Mayor Jack Gilfoy, and tour the newly expanded and renovated health center. </p>
		<p>VPCHC was awarded grand funding in 2011, through the American Recovery and Reinvestment Act (ARRA) Health Center Capital Improvement Program Initiative. The purpose of this award is to expand availability for needed primary health care services, specifically by expanding services at existing health center sites in an underserved medical area. The awarded grant funding was designed to allow VPCHC to increase services at its existing locations, address the growth in demand to serve patients in the local community and surrounding areas, to improve health information technology, and conduct renovations and construction for the growth and development of its services.</p>
		<p>The facility construction and renovation have allowed for the addition and increase of patient exam rooms, larger waiting room area, and additional on-site services, with an increase from 2,700 to 8,000 square feet of facility space for growth of the health center. Construction was completed by Garmong Construction Services, in renovated space owned by Union Hospital.</p>
		<p>&ldquo;We are extremely excited to offer this expanded and renovated facility to better serve all of our primary and behavioral health patients.    With 19 exam rooms, we will be able to serve more patients in an efficient manner,&rdquo; stated Elizabeth Burrows, CEO of the health center.</p>
		<p>The Open House will take place on Wednesday, October 10th, from 11:30 a.m. to 1:30 p.m., and will be held at 777 S. Main St., Ste 100, Clinton, Ind.  The event is sponsored by First Financial Bank Clinton.</p>
		<p>Vermillion-Parke Community Health Center is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
